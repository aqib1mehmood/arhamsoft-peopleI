    public function index(Request $request)
    {

        $awards = AwardType::where('status', '1')->where('client_id', $request->client_id)->get(['id', 'name']);
        $count = $awards->count();
        return response()->json([
            'success' => $count > 0 ? true : false,
            'message' => $count > 0 ? 'Awards Type Found' : 'No Awards Type Found!',
            'results' => $count > 0 ? $awards : null,
        ], );
    }

